interface LangfuseMessage {
  role?: string;
  content?: string;
}

interface LangfuseDatasetItem {
  id: string;
  sourceObservationId?: string;
  input?: unknown;
  expectedOutput?: {
    hasTransaction?: boolean;
  };
}

interface GeneratedDatasetCase {
  datasetItemId: string;
  sourceObservationId?: string;
  userFullName?: string;
  userLocale?: string;
  emailContent: string;
  expectedFromExportHasTransaction: boolean;
}

const OWNER_REGEX = /email recipient\/account owner is:\s*(.+?)\n/i;
const USER_LOCALE_REGEX = /reason" field in this language:\s*([a-z-]+)/i;
const EMAIL_MARKER = "Email to analyze:";

function escapeTemplateLiteral(text: string): string {
  return text
    .replaceAll("\\", "\\\\")
    .replaceAll("`", "\\`")
    .replaceAll("${", "\\${");
}

function extractUserMessage(input: unknown): string {
  if (!Array.isArray(input)) return "";

  for (const message of input as LangfuseMessage[]) {
    if (message?.role === "user" && typeof message.content === "string") {
      return message.content;
    }
  }

  return "";
}

function buildCase(item: LangfuseDatasetItem): GeneratedDatasetCase {
  const userMessage = extractUserMessage(item.input);
  const ownerMatch = OWNER_REGEX.exec(userMessage);
  const userLocaleMatch = USER_LOCALE_REGEX.exec(userMessage);
  const markerIndex = userMessage.indexOf(EMAIL_MARKER);
  const emailContent =
    markerIndex >= 0
      ? userMessage.slice(markerIndex + EMAIL_MARKER.length).trim()
      : userMessage.trim();

  return {
    datasetItemId: item.id,
    sourceObservationId: item.sourceObservationId,
    userFullName: ownerMatch?.[1]?.trim(),
    userLocale: userLocaleMatch?.[1]?.trim(),
    emailContent,
    expectedFromExportHasTransaction:
      item.expectedOutput?.hasTransaction === true,
  };
}

function renderFile(cases: GeneratedDatasetCase[]): string {
  const items = cases
    .map(datasetCase => {
      const sourceObservationLine = datasetCase.sourceObservationId
        ? `  sourceObservationId: "${datasetCase.sourceObservationId}",\n`
        : "";
      const userFullNameLine = datasetCase.userFullName
        ? `  userFullName: "${datasetCase.userFullName}",\n`
        : "";
      const userLocaleLine = datasetCase.userLocale
        ? `  userLocale: "${datasetCase.userLocale}",\n`
        : "";

      return `{
  datasetItemId: "${datasetCase.datasetItemId}",
${sourceObservationLine}${userFullNameLine}${userLocaleLine}  expectedFromExportHasTransaction: ${datasetCase.expectedFromExportHasTransaction},
  emailContent: \`${escapeTemplateLiteral(datasetCase.emailContent)}\`,
}`;
    })
    .join(",\n\n");

  return `// Auto-generated by import-langfuse-dataset.ts
// Do not edit manually. Re-generate from Langfuse export when needed.

export interface LangfuseDatasetGeneratedCase {
  datasetItemId: string;
  sourceObservationId?: string;
  userFullName?: string;
  userLocale?: string;
  emailContent: string;
  expectedFromExportHasTransaction: boolean;
}

export const langfuseDatasetGeneratedCases: LangfuseDatasetGeneratedCase[] = [
${items}
];
`;
}

if (import.meta.main) {
  const [inputPath, outputPath] = Deno.args;

  if (!inputPath) {
    console.error(
      "Usage: deno run --allow-read --allow-write import-langfuse-dataset.ts <input-json-path> [output-ts-path]"
    );
    Deno.exit(1);
  }

  const resolvedOutputPath =
    outputPath ??
    new URL("./langfuse-dataset-cases.generated.ts", import.meta.url).pathname;

  const raw = await Deno.readTextFile(inputPath);
  const parsed = JSON.parse(raw);
  if (!Array.isArray(parsed)) {
    throw new Error("Expected dataset export to be an array.");
  }

  const generatedCases = parsed.map(item =>
    buildCase(item as LangfuseDatasetItem)
  );

  const fileContent = renderFile(generatedCases);
  await Deno.writeTextFile(resolvedOutputPath, fileContent);

  console.log(
    `Generated ${generatedCases.length} cases at ${resolvedOutputPath}`
  );
}
