// Locale-aware currency formatter used inside the PDF text. Built on
// top of Intl.NumberFormat so the same backend produces ES-locale
// "1.234,56" vs EN-locale "1,234.56" without depending on the
// frontend's currency.ts utility.
function makeFormatCurrency(locale: "en" | "es") {
  const intlLocale = locale === "es" ? "es-AR" : "en-US";
  const cache = new Map<string, Intl.NumberFormat>();
  return (amount: number, currency: string): string => {
    let fmt = cache.get(currency);
    if (!fmt) {
      fmt = new Intl.NumberFormat(intlLocale, {
        style: "currency",
        currency,
        currencyDisplay: "symbol",
      });
      cache.set(currency, fmt);
    }
    return fmt.format(amount);
  };
}

function makeFormatDate(locale: "en" | "es") {
  const intlLocale = locale === "es" ? "es-AR" : "en-US";
  return (date: string | null): string => {
    if (!date) return "";
    const d = new Date(`${date}T00:00:00Z`);
    return new Intl.DateTimeFormat(intlLocale, {
      year: "numeric",
      month: "2-digit",
      day: "2-digit",
    }).format(d);
  };
}

function slugifyFilename(title: string): string {
  const slug = title
    .toLowerCase()
    .normalize("NFKD")
    .replace(/[\u0300-\u036f]/g, "")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "")
    .slice(0, 60);
  const stamp = new Date().toISOString().split("T")[0];
  return `report-${slug || "untitled"}-${stamp}.pdf`;
}

Deno.serve(async req => {
  const preflight = handleCorsPreflightRequest(req);
  if (preflight) return preflight;
  const corsHeaders = getCorsHeaders(req);

  if (req.method !== "GET") {
    return new Response(JSON.stringify({ error: "Method not allowed" }), {
      status: 405,
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }

  try {
    // 1. auth
    const auth = await requireUserAuth(req, corsHeaders);
    if (auth instanceof Response) return auth;
    const { user, role } = auth;
    void user;

    // 2. capability gate
    const cap = await requireCapability(
      { user, role },
      CAPABILITIES.report_pdf_export,
      corsHeaders
    );
    if (cap instanceof Response) return cap;

    // 3. parse + validate reportId
    const url = new URL(req.url);
    const reportId = url.searchParams.get("reportId");
    if (!reportId) {
      return new Response(
        JSON.stringify({ error: "Missing reportId query parameter" }),
        {
          status: 400,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        }
      );
    }
    const langParam = url.searchParams.get("lang");
    const locale: "en" | "es" =
      langParam === "es" || req.headers.get("x-user-locale")?.startsWith("es")
        ? "es"
        : "en";

    // 4. usage cap — check limit only (don't increment yet). We
    // increment AFTER a successful PDF build so that errors (bad PNG,
    // encoding, etc.) don't burn the user's quota.
    if (role !== "admin") {
      const { data: limitRows, error: limitError } = await serviceRoleSupabase
        .schema("payments")
        .rpc("resolve_usage_limit", {
          target_user_id: auth.user.id,
          cap: USAGE_CAPABILITY,
        });
      if (!limitError && limitRows && (limitRows as number) <= 0) {
        return new Response(
          JSON.stringify({
            error: `Usage limit exceeded: ${USAGE_CAPABILITY}`,
          }),
          {
            status: 429,
            headers: { ...corsHeaders, "Content-Type": "application/json" },
          }
        );
      }
    }

    // 5. fetch the report — by-id with ownership check via RLS.
    const { data: report, error: reportError } = await serviceRoleSupabase
      .from("reports")
      .select(
        "id, user_id, title, description, status, date_range_start, date_range_end, created_at, archived_at"
      )
      .eq("id", reportId)
      .single();
    if (reportError || !report) {
      return new Response(JSON.stringify({ error: "Report not found" }), {
        status: 404,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }
    // Defense in depth: RLS already enforces user_id match, but be
    // explicit in case the policy regresses.
    if (report.user_id !== auth.user.id) {
      return new Response(JSON.stringify({ error: "Report not found" }), {
        status: 404,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    // 6. fetch transactions in this report (paginate — cap at
    // 1000 rows so a runaway report can't blow up the Edge function).
    const PAGE = 1000;
    const { data: txs, error: txsError } = await serviceRoleSupabase
      .from("transactions")
      .select(
        "id, name, merchant, amount, currency, transaction_type, transaction_date, category"
      )
      .eq("report_id", reportId)
      .eq("discarded", false)
      .order("transaction_date", { ascending: false })
      .range(0, PAGE - 1);
    if (txsError) {
      console.error("[export-report-pdf] transactions fetch failed", txsError);
      return new Response(
        JSON.stringify({ error: "Failed to load transactions" }),
        {
          status: 500,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        }
      );
    }

    // 7. compute per-currency summary from the already-fetched
    // transactions. Avoids the get_report_summaries RPC which depends
    // on auth.uid() — our service-role client doesn't set that.
    const perCurrencyMap = new Map<
      string,
      { count: number; income: number; expenses: number }
    >();
    for (const t of txs ?? []) {
      const c = t.currency ?? "USD";
      const entry = perCurrencyMap.get(c) ?? {
        count: 0,
        income: 0,
        expenses: 0,
      };
      entry.count += 1;
      if (t.transaction_type === "income" || t.transaction_type === "ingreso") {
        entry.income += Number(t.amount ?? 0);
      } else {
        entry.expenses += Number(t.amount ?? 0);
      }
      perCurrencyMap.set(c, entry);
    }
    const perCurrency = Array.from(perCurrencyMap.entries()).map(
      ([currency, v]) => ({
        currency,
        transactionCount: v.count,
        totalIncome: v.income,
        totalExpenses: v.expenses,
        net: v.income - v.expenses,
      })
    );

    // 8. build the PDF
    const labels = PDF_LABELS[locale];
    const pdfBytes = await buildReportPdf({
      report: {
        id: report.id,
        title: report.title,
        description: report.description,
        status: report.status,
        dateRangeStart: report.date_range_start,
        dateRangeEnd: report.date_range_end,
        createdAt: report.created_at,
        archivedAt: report.archived_at,
      },
      transactions: (txs ?? []).map(t => ({
        id: t.id,
        name: t.name,
        merchant: t.merchant ?? "",
        amount: Number(t.amount ?? 0),
        currency: t.currency ?? "USD",
        transactionDate: t.transaction_date ?? "",
        category: t.category ?? "",
        transactionType: t.transaction_type ?? "expense",
      })),
      perCurrency,
      labels,
      formatCurrency: makeFormatCurrency(locale),
      formatDate: makeFormatDate(locale),
    });

    // 10. increment usage counter — only AFTER a successful build.
    if (role !== "admin") {
      await serviceRoleSupabase
        .schema("payments")
        .rpc("check_and_increment_usage", {
          target_user_id: auth.user.id,
          cap: USAGE_CAPABILITY,
        });
    }

    // 11. respond with PDF binary. The Content-Disposition makes the
    // browser trigger a download with our filename.
    const filename = slugifyFilename(report.title);
    const headers = {
      ...corsHeaders,
      "Content-Type": "application/pdf",
      "Content-Length": pdfBytes.length.toString(),
      "Content-Disposition": `attachment; filename="${filename}"`,
      "Cache-Control": "no-store",
    };

    return new Response(pdfBytes, { status: 200, headers });
  } catch (err) {
    console.error("[export-report-pdf] unhandled error:", err);
    return new Response(
      JSON.stringify({
        error: "Internal server error",
      }),
      {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      }
    );
  }
});
