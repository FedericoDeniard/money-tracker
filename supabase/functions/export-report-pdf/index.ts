// export-report-pdf Edge Function
//
// Returns a downloadable PDF of a user-owned report. Used by the
// "Download PDF" button on /reports/:reportId. The build is server-
// side via pdf-lib so the file works identically across clients,
// doesn't ship JS to the browser, and is easier to audit (every byte
// originates on our infrastructure).
//
// Pipeline:
//   1. requireUserAuth — JWT must be valid (mirrors every other function)
//   2. requireCapability('report_pdf_export') — gates the route
//      against users without the capability (free users also get this
//      via payments.default_capabilities; the gate is paid-vs-free's
//      cheaper cousin — same capability table)
//   3. check_and_increment_usage — admin bypass; everyone else counts
//      against the per-period cap in payments.usage_limits
//      (default = 10/month; tester = 500/month). A 429 from the rpc
//      surfaces as the localized "you've reached your monthly limit"
//      toast on the client via getEdgeFunctionErrorMessage.
//   4. fetch report + transactions from public.transactions / public.reports
//   5. fetch perCurrency summary via the get_report_summaries rpc
//   6. buildReportPdf() from _shared/lib/report-pdf → Uint8Array
//      (watermark PNG is embedded as a base64 constant — no fs deps)
//   7. respond with application/pdf + Content-Disposition: attachment
//
// Locale: the frontend sends `?lang=es|en` (default `en`); the labels
// object passed to buildReportPdf is one of two hardcoded maps. We
// deliberately don't bring in i18next — a 15-key literal map is
// cheaper than ~50KB of i18n runtime pulled into the Edge function.
//
// URL: GET /functions/v1/export-report-pdf?reportId=<uuid>&lang=es|en
import "jsr:@supabase/functions-js/edge-runtime.d.ts";
import { requireUserAuth } from "../_shared/auth.ts";
import { CAPABILITIES, requireCapability } from "../_shared/capabilities.ts";
import { getCorsHeaders, handleCorsPreflightRequest } from "../_shared/cors.ts";
import { buildReportPdf, type PdfLabel } from "../_shared/lib/report-pdf.ts";
import { supabase as serviceRoleSupabase } from "../_shared/lib/supabase.ts";

// Per-capability usage counter name — matches the row inserted by the
// migration that grants the capability. Kept here (and not in
// CAPABILITIES) because capability keys are identifiers, not labels.
const USAGE_CAPABILITY = "report_pdf_export";

